def save_articles(articles: list):
    """–°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Å—Ç–∞—Ç–µ–π"""
    ARTICLES_DIR.mkdir(parents=True, exist_ok=True)
