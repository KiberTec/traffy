def generate_article_id(title: str) -> str:
    """–ì–µ–Ω–µ—Ä–∞—Ü–∏—è —É–Ω–∏–∫–∞–ª—å–Ω–æ–≥–æ ID —Å—Ç–∞—Ç—å–∏ –∏–∑ –∑–∞–≥–æ–ª–æ–≤–∫–∞"""
    # –¢—Ä–∞–Ω—Å–ª–∏—Ç–µ—Ä–∞—Ü–∏—è –∏ –æ—á–∏—Å—Ç–∫–∞
    translit = {
        '–∞': 'a', '–±': 'b', '–≤': 'v', '–≥': 'g', '–¥': 'd', '–µ': 'e', '—ë': 'e',
        '–∂': 'zh', '–∑': 'z', '–∏': 'i', '–π': 'y', '–∫': 'k', '–ª': 'l', '–º': 'm',
        '–Ω': 'n', '–æ': 'o', '–ø': 'p', '—Ä': 'r', '—Å': 's', '—Ç': 't', '—É': 'u',
        '—Ñ': 'f', '—Ö': 'h', '—Ü': 'ts', '—á': 'ch', '—à': 'sh', '—â': 'sch',
        '—ä': '', '—ã': 'y', '—å': '', '—ç': 'e', '—é': 'yu', '—è': 'ya'
    }
    
    result = title.lower()
    for ru, en in translit.items():
        result = result.replace(ru, en)
    
    # –û—Å—Ç–∞–≤–ª—è–µ–º —Ç–æ–ª—å–∫–æ –±—É–∫–≤—ã, —Ü–∏—Ñ—Ä—ã –∏ –ø—Ä–æ–±–µ–ª—ã, –∑–∞–º–µ–Ω—è–µ–º –ø—Ä–æ–±–µ–ª—ã –Ω–∞ –¥–µ—Ñ–∏—Å—ã
    result = ''.join(c if c.isalnum() or c == ' ' else '' for c in result)
    result = '-'.join(result.split())
    
    # –î–æ–±–∞–≤–ª—è–µ–º –¥–∞—Ç—É –¥–ª—è —É–Ω–∏–∫–∞–ª—å–Ω–æ—Å—Ç–∏
    date_suffix = datetime.now().strftime("%Y%m%d")
    return f"{result[:50]}-{date_suffix}"


def load_existing_articles() -> list:
    """–ó–∞–≥—Ä—É–∑–∫–∞ —Å—É—â–µ—Å—Ç–≤—É—é—â–∏—Ö —Å—Ç–∞—Ç–µ–π"""
    if ARTICLES_JSON.exists():
        with open(ARTICLES_JSON, 'r', encoding='utf-8') as f:
            return json.load(f)
    return []


def save_articles(articles: list):
    """–°–æ—Ö—Ä–∞–Ω–µ–Ω–∏–µ —Å—Ç–∞—Ç–µ–π –≤ JSON"""
    with open(ARTICLES_JSON, 'w', encoding='utf-8') as f:
        json.dump(articles, f, ensure_ascii=False, indent=2)


def get_unused_topic(existing_articles: list) -> tuple:
    """–ü–æ–ª—É—á–∏—Ç—å —Ç–µ–º—É, –∫–æ—Ç–æ—Ä–∞—è –µ—â—ë –Ω–µ –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–ª–∞—Å—å"""
    existing_titles = {a['title'].lower() for a in existing_articles}
    
    # –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º –∫–∞—Ç–µ–≥–æ—Ä–∏–∏ –∏ —Ç–µ–º—ã
    all_topics = []
    for cat_data in TOPICS:
        for topic in cat_data['topics']:
            all_topics.append((cat_data['category'], topic))
    
    random.shuffle(all_topics)
    
    for category, topic in all_topics:
        if topic.lower() not in existing_titles:
            return category, topic
    
    # –ï—Å–ª–∏ –≤—Å–µ —Ç–µ–º—ã –∏—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω—ã - –≤—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—É—é
    category, topic = random.choice(all_topics)
    return category, f"{topic} (–æ–±–Ω–æ–≤–ª–µ–Ω–∏–µ {datetime.now().year})"


